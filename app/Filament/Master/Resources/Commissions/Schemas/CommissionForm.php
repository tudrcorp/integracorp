                TextInput::make('date_payment_affiliate')
                    ->required(),
                TextInput::make('agency_id')
                    ->numeric(),
                TextInput::make('code_agency'),
                TextInput::make('owner_code'),
                TextInput::make('agent_id')
                    ->numeric(),
                TextInput::make('plan_id')
                    ->required()
                    ->numeric(),
                TextInput::make('coverage_id')
                    ->numeric(),
                TextInput::make('sale_id')
                    ->required()
                    ->numeric(),
                TextInput::make('invoice_number')
                    ->required(),
                TextInput::make('affiliate_full_name')
                    ->required(),
                TextInput::make('amount')
                    ->required()
                    ->numeric(),
                TextInput::make('payment_method')
                    ->required(),
                TextInput::make('veto')
                    ->required()
                    ->default('ANUAL'),
                TextInput::make('payment_frequency')
                    ->required(),
                TextInput::make('commission_agency_master')
                    ->required()
                    ->numeric()
                    ->default(0.0),
                TextInput::make('commission_agency_general')
                    ->required()
                    ->numeric()
                    ->default(0.0),
                TextInput::make('commission_agent')
                    ->required()
                    ->numeric()
                    ->default(0.0),
                TextInput::make('total_payment_commission')
                    ->required()
                    ->numeric()
                    ->default(0.0),
                TextInput::make('date_payment_commission')
                    ->required(),
