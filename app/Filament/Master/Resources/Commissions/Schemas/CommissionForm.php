                    ->numeric()
                    ->default(0.0),
                TextInput::make('commission_agency_general_ves')
                    ->numeric()
                    ->default(0.0),
                TextInput::make('commission_agent_ves')
                    ->numeric()
                    ->default(0.0),
                TextInput::make('date_ini'),
                TextInput::make('date_end'),
            ]);
    }
}
