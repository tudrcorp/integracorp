                    ->numeric()
                    ->default(0.0),
                TextInput::make('pay_amount_usd')
                    ->numeric()
                    ->default(0.0),
                TextInput::make('pay_amount_ves')
                    ->numeric()
                    ->default(0.0),
                TextInput::make('payment_method_usd'),
                TextInput::make('payment_method_ves'),
                TextInput::make('commission_agency_master_usd')
                    ->numeric()
                    ->default(0.0),
                TextInput::make('commission_agency_general_usd')
                    ->numeric()
                    ->default(0.0),
                TextInput::make('commission_agent_usd')
                    ->numeric()
                    ->default(0.0),
